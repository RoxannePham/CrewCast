export type UserRole = 'worker' | 'host';
export type WorkerRole = 'DJ' | 'Photographer' | 'Videographer' | 'Host/MC' | 'Content Creator' | 'Bartender' | 'Security' | 'Volunteer' | 'Setup Crew';

export interface WorkerProfile {
  id: string;
  type: 'worker';
  name: string;
  email: string;
  age: number;
  gender: string;
  portraitPath: any;
  avatarColor: string;
  school: string;
  city: string;
  bio: string;
  roles: WorkerRole[];
  skills: string[];
  yearsExperience: number;
  gigs: number;
  rating: number;
  reliabilityScore: number;
  onTimePercent: number;
  completionPercent: number;
  responseSpeed: number;
  repeatBookings: number;
  badges: string[];
  isTopRated: boolean;
  isOnTime: boolean;
  isCampusVerified: boolean;
  hasEquipment: boolean;
  canTravel: boolean;
  portfolioLinks: string[];
  socialLinks: { instagram?: string; tiktok?: string; linkedin?: string };
  preferredEventTypes: string[];
  availability: string[];
  reviews: Review[];
}

export interface HostProfile {
  id: string;
  type: 'host';
  name: string;
  email: string;
  portraitPath: any;
  avatarColor: string;
  orgName: string;
  orgRole: string;
  school: string;
  city: string;
  eventTypes: string[];
  budgetRange: string;
  eventsHosted: number;
  rating: number;
}

export interface Review {
  id: string;
  authorId: string;
  authorName: string;
  authorAvatarColor: string;
  rating: number;
  text: string;
  date: string;
  eventTitle: string;
}

function calcReliability(onTime: number, completion: number, rating: number, response: number, repeat: number): number {
  return parseFloat(((0.25 * onTime) + (0.25 * completion) + (0.25 * (rating / 5)) + (0.15 * response) + (0.10 * repeat)).toFixed(2));
}

